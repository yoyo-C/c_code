#include <iostream>
using namespace std;

bool A(int a[]){
	bool n1, n2, n3;
	n1 = (a[1] == 1 && a[3] != 4 && a[0] != 3);
	n2 = (a[1] != 1 && a[3] == 4 && a[0] != 3);
	n3 = (a[1] != 1 && a[3] != 4 && a[0] == 3);
	return (n1 || n2 || n3); 
}
bool B(int a[]){
	bool n1, n2, n3, n4;
	n1 = (a[3] == 1 && a[1] != 4 && a[0] != 2 && a[2] != 3);
	n2 = (a[3] != 1 && a[1] == 4 && a[0] != 2 && a[2] != 3);
	n3 = (a[3] != 1 && a[1] != 4 && a[0] == 2 && a[2] != 3);
	n4 = (a[3] != 1 && a[1] != 4 && a[0] != 2 && a[2] == 3);
	return (n1 || n2 || n3 || n4);
}
bool C(int a[]){
	bool n1, n2;
	n1 = (a[1] == 3 && a[3] != 4);
	n2 = (a[1] != 3 && a[3] == 4);
	return (n1 || n2);
} 
bool D(int a[]){
	bool n1, n2, n3, n4;
	n1 = (a[3] == 2 && a[1] != 3 && a[0] != 1 && a[2] != 4);
	n2 = (a[3] != 2 && a[1] == 3 && a[0] != 1 && a[2] != 4);
	n3 = (a[3] != 2 && a[1] != 3 && a[0] == 1 && a[2] != 4);
	n4 = (a[3] != 2 && a[1] != 3 && a[0] != 1 && a[2] == 4);
	return (n1 || n2 || n3 || n4);
}
int main(){
	int a[4];
	for (a[0] = 1; a[0] < 5; a[0]++){
		for (a[1] = 1; a[1] < 5; a[1]++){
			if (a[1] == a[0]){
				continue;
			}
			for (a[2] = 1; a[2] < 5; a[2]++){
				if (a[2] == a[0] || a[2] == a[1]){
					continue;
				}
				for (a[3] = 1; a[3] < 5; a[3]++){
					if (a[3] == a[2] || a[3] == a[0] || a[3] == a[1]){
						continue;
					}
					if (A(a) && B(a) && C(a) && D(a)){
						cout << a[0] << "\n" << a[1] << "\n" << a[2] << "\n" << a[3] << endl;
						return 0;
					}
				}
			}
		}
	}
	return 0;
}